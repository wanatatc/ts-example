type Fish = {swim: () => void};
type Bird = {fly: () => void};

function move(animal: Fish | Bird) {
  if ("swim" in animal) {
    // ‡∏ñ‡πâ‡∏≤‡∏°‡∏µ property swim ‡πÉ‡∏ô animal
    return animal.swim();
  }

  return animal.fly();
}

